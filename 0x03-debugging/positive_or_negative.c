#include <stdio.h>

/**
 * largest_number - returns the largest of 3 numbers
 * @n: first integer
 * Return: largest number
 */

positive_or_negative(n)
{
if (n > 0)
  {
printf("%d is positive\n", n);
}
 else if (n == 0)
   {
printf("%d is zero\n", n);
}
 else
   {
printf("%d is negative\n", n);
}
}
